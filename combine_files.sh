#!/bin/bash

# Название выходного файла
output_file="combined.txt"

# Очистка или создание выходного файла
> "$output_file"

# Цикл по всем файлам с нужными расширениями
for file in *.pro *.cpp *.h *.ui; do
    # Проверка, существует ли файл
    if [[ -f "$file" ]]; then
        echo "Добавление содержимого файла: $file"
        # Добавление содержимого файла в выходной файл
        cat "$file" >> "$output_file"
        # Добавление разделителя (например, новой строки) между файлами
        echo -e "\n" >> "$output_file"
    fi
done

echo "Объединение завершено. Содержимое сохранено в $output_file."

